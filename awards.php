						<h3 class="animate-onscroll no-margin-top" id="young-scientist-award">Young Scientist Award</h3>
						<p>To promote the budding young scientists, the conference encourages the participants to apply for following awards. The applicants have to put their choices while submitting the abstract online. The awards are listed below:</p>
						<p><b>'BioSangam' Young Scientist Award:</b>
						One 'BioSangam' Young Scientist Award will be given at the conference. The participant should be registered in the conference and below 35 years of age at the time of the application. The applicant for ‘BioSangam Young Scientist Award’ would send their printed registration form, detailed abstract along with their curriculum vitae forwarded by head of the department/ supervisor to the conference secretriat.</p>
						<p>Only for BioSangam Young Scientist Award, the abstract should be structured (Background, Methods, Results and conclusions)and should not be more than 500 words. Partcipants should also highlight the implications of their study. If their work has already been published, a copy of article should also be submitted. A soft copy of the abstract should also be sent.</p>
						<p id="best-oral-presentation">The award carries a certificate, a medal and cash prize. The screened candidates will be informed by E-mail and have to submit no-objection certificate from co-authors. The oral presentation of their research work will be judged by the expert panel and the best one will be chosen for the award. The award will be distributed at the valedictory function of the conference.</p>
							<h3 >Best Oral Presentation Award</h3>
							<p><b>Six Best Oral Presentations:</b>The sessions based on nine broad themes will comprise of 'six best abstracts' submitted to the conference selected by a panel of experts. One best presenter in each session will get cash award each along with a certificate.</p>
							<p id="poster-presentation">Interested participants should first register, make payment and then submit the abstract online using their login account. The word limit of the abstract is 250 words.</p>
							<h3 >Poster Presentation</h3>
							<p><b>Best Poster Presentation:</b>Posters will be put up during the conference. Dedicated time slot has been kept for viewing and presenters are advised to be available for discussions with the judges. Poster presentation award will include cash award along with a certificate.</p>
						<p>Interested participants should first register, make payment and then submit the abstract online using their login account. The word limit of the abstract is 250 words.</p>

						
						
