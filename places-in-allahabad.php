						<h3 class="animate-onscroll no-margin-top">Tourist Places to Visit</h3>
						<p><b>Places to visit:</b> The 'Kumbh Nagari' Allahabad is major tourist attraction in India from all over the world. The city's original name—Prayaga, or "place of sacrifice", comes from its position at the sacred union of the rivers Ganges, Yamuna and Saraswati. It is the second-oldest city in India and plays a central role in the Hindu scriptures.</p>
						<p>The conference secretariat may offer a tour package to visit the nearby places on request only. Do write us on <a href="mailto:biosangam@mnnit.ac.in" >biosangam@mnnit.ac.in</a> The major tourist attractions in the city are :</p>
						<br>

						<img class="align-left animate-onscroll" src="img/sangam.jpg" alt="">
						<p class="animate-onscroll"><b>Sangam:</b> Sangam is the meeting of three rivers: river Ganga, river Yamuna, and the mythical and invisible Saraswathi. The sacred union offers excellent view during the sunrise and the sunset. Kumbh Mela (held once in 12 years) has been recently organized from January- March 2013.</p>
						
						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/ald-fort.jpg" alt="">
						<p class="animate-onscroll"><b>Allahabad Fort:</b> The Fort is situated on the bank of the Yamuna, near to Sangam. This Mugal fort has three magnificent galleries flanked by high towers. This fort was built by the great Moghul king Akbar in 1583.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/ald-museum.jpg" alt="">
						<p class="animate-onscroll"><b>Allahabad Museum:</b> The museum is located beside the Chandra Shekar Azab park.</p>

						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/anand-bhawan.jpg" alt="">
						<p class="animate-onscroll"><b>Anand Bhavan:</b>  Anand Bhavan a large mansion established by prominent Indian political leader Motilal Nehru in the nineteenth century.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/swaraj-bhawan.jpg" alt="">
						<p class="animate-onscroll"><b>Swaraj Bhawan:</b> The old Anand Bhavan, which in 1930 was donated by Motilal Nehru, a freedom fighter and father of Jawaharlal Nehru, is renamed by Motilal Nehru as Swaraj Bhawan.</p>

						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/ashoka-pillar.jpg" alt="">
						<p class="animate-onscroll"><b>Ashoka Pillar:</b>Standing tall inside the Allahabad Fort, built by Akbar, is the polished sandstone Ashoka Pillar. This pillar was earthed by the order of the great Mauryan king Ashoka.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/khusro-bagh.jpg" alt="">
						<p class="animate-onscroll"><b>Khusro Bagh:</b> This garden is a walled historic garden created by the Moghul king Jahangir, in memory of his beloved son Khusro. Khusro was murdered by his brother Shah Jahan. The garden has the tomb of the murdered prince.</p>

						<p style="clear:both"></p>
						
						<hr style="border-top:1px solid gray">
						<p>The major tourist attractions around the city are :</p>
						<p><h3>Varanasi:</h3>
							 Varanasi is a city on the banks of the Ganges in Uttar Pradesh, 120 km far from Allahabad. The old name of the city is 'Benaras'. Hindus believe that death at Varanasi brings salvation. It is one of the oldest continuously inhabited cities in the world and the oldest in India. The Benares Gharana form of Hindustani classical music developed in Varanasi, and many prominent Indian philosophers, poets, writers, and musicians live or have lived in Varanasi. Gautama Buddha gave his first sermon at Sarnath located near Varanasi.

						</p>
						<img class="align-left animate-onscroll" src="img/bhu.jpg" alt="">
						<p class="animate-onscroll"><b>Banaras Hindu University: </b> Banaras Hindu University is an internationally reputed oldest place of learning, situated in the holy city of Varanasi. This Creative and innovative university was founded by the great nationalist leader, Pandit Madan Mohan Malviya. The area of the main campus of this premiere Central University is 1300 acres, having well maintained roads, extensive greenery, a temple, an air strip and buildings which are an architectural delight.</p>
						
						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/ganga-ghat.jpg" alt="">
						<p class="animate-onscroll"><b>Ganga Ghat:</b>Ghats are embankments made in steps of stone slabs along the river bank where pilgrims perform ritual ablutions. Ghats in Varanasi are an integral complimentary to the concept of divinity represented in physical, metaphysical and supernatural elements. The Dashashwamedh Ghat is the main and probably the oldest ghat of Varanasi located on the Ganges. 'The Arti' at this ghat at sunset with chanting of 'Mantra' is worth watching.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/sarnath.jpg" alt="">
						<p class="animate-onscroll"><b>Sarnath: </b>Sarnath is located 13 kilometres north-east of Varanasi in Uttar Pradesh, India where Gautama Buddha first taught the Dharma. This place was the birthplace of Shreyansanath, the eleventh Tirthankara of Jainism, and a temple dedicated to him, is an important pilgrimage site.</p>

						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/vishwanath-temple.jpg" alt="">
						<p class="animate-onscroll"><b>Old Vishwanath Temple: </b>This Kashi Vishwanath Temple – dedicated to Varanasi's presiding deity Shiva is an important Hindu temple and one of the 12 Jyotirlinga Shiva temples. The temple has been destroyed and rebuilt a number of times.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/SankatMochanHanumanTemple.jpg" alt="">
						<p class="animate-onscroll"><b>Sankat Mochan Hanuman Temple:</b> The Sankat Mochan Hanuman Temple is one of the sacred temples of the Hindu god Hanuman.</p>

						<p style="clear:both"></p>
						

						<img class="align-right animate-onscroll" src="img/birla-vishwanath-temple-varanasi.jpg" alt="">
						<p class="animate-onscroll"><b>Birla Vishwanath Temple:</b>The New Vishwanath Temple located in the campus of Banaras Hindu University is a modern temple which was planned by Pandit Malviya and built by the Birlas.</p>

						<p style="clear:both"></p>
						

						<img class="align-left animate-onscroll" src="img/vindhyanchal-temple.jpg" alt="">
						<p class="animate-onscroll"><b>Vindhyachal Temple:</b>The dignified temple of Vindhyachal Mata is very near to Allahabad in District Mirzapur (60-70 km from the venue).</p>

						<p style="clear:both"></p>
						<hr style="border-top:1px solid gray">
						<br class="clearfix">
						
						<img class="align-right animate-onscroll" src="img/taj-mahal.jpg" alt="">
						<p class="animate-onscroll"><b>Taj Mahal and Agra: </b>Taj Mahal is one of the most famous buildings in the world, the mausoleum of Shah Jahan's favorite wife, Mumtaz Mahal. It is one of the New Seven Wonders of the world, and one of three World Heritage Sites in Agra. Agra is on the train line between New Delhi (approx. 200 km) and Allahabad (approx. 350 km).</p>
						<p style="clear:both"></p>


						<img class="align-left animate-onscroll" src="img/chitrakoot.jpg" alt="">
						<p class="animate-onscroll"><b>Chitrakoot: </b>Chitrakoot is a town of religious, cultural, histoical and archaeological importance. It is known for a number of temples, sites mentioned in Hindu scriptures and relevance in 'Ramayana'.</p>
